import math
# ---------------
# square rooting numbers

print('-- square rooting numbers --')

x = math.sqrt(117)
print(x)

print(math.sqrt(81))

# ---------------
# flooring (rounding) numbers

print('-- rounding numbers --')

y = math.floor(math.sqrt(x))
print(y)

print(math.floor(8.3))
print(math.floor(9.5))
print(math.floor(4.8))